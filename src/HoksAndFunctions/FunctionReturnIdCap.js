export function retornandoIdCapa(produtos){
    if(produtos.id === 312 ){
        return 0
    }

    else if(produtos.id === 201){
        return 1
    }

    else if(produtos.id  === 102){
        return 2
    }

    else if(produtos.id  === 99){
        return 3
    }

    else if(produtos.id  === 12){
        return 4
    }

    else if(produtos.id  === 74){
        return 5
    }

    else if(produtos.id  === 31){
        return 6
    }

    else if(produtos.id  === 420){
        return 7
    }

    else if(produtos.id === 501){
        return 8
    }
};